package main

import (
	"bufio"
	"fmt"
	"io"
	"log"
	"net"
	"strings"
)

func main() {
	li, err := net.Listen("tcp", ":8080")
	if err != nil {
		log.Panic(err)
	}
	defer li.Close()

	for {
		conn, err := li.Accept()
		if err != nil {
			log.Println(err)
			continue
		}
		serve(conn)
	}
}

func serve(conn net.Conn) {
	defer conn.Close()
	scanner := bufio.NewScanner(conn)
	var i int
	var reqMethod, reqURI string
	for scanner.Scan() {
		ln := scanner.Text()
		fmt.Println(ln)
		if i == 0 {
			// Request Line
			xs := strings.Fields(ln)
			reqMethod = xs[0]
			reqURI = xs[1]
			fmt.Println("METHOD", reqMethod)
			fmt.Println("URI", reqURI)
		}
		if ln == "" {
			break
		}
		i++
	}

	switch {
	case reqMethod == "GET" && reqURI == "/":
		handleIndex(conn)
	case reqMethod == "GET" && reqURI == "/apply":
		handleApply(conn)
	case reqMethod == "POST" && reqURI == "/apply":
		handleApplyPost(conn)
	default:
		handleDefault(conn)
	}

}

func handleIndex(conn net.Conn) {
	body := `
		<!DOCTYPE html>
		<html lang="en">
		<head>
			<meta charset="UTF-8">
			<title>Code Gangsta</title>
		</head>
		<body>
			<h1>"GET INDEX"</h1>
			<a href="/">index</a><br>
			<a href="/apply">apply</a><br>
		</body>
		</html>
	`
	io.WriteString(conn, "HTTP/1.1 200 OK\r\n")
	fmt.Fprintf(conn, "Content-Length: %d\r\n", len(body))
	fmt.Fprint(conn, "Content-Type: text/html\r\n")
	io.WriteString(conn, "\r\n")
	io.WriteString(conn, body)
}

